function errs = run_alg_params(data_train, data_test, C, bandwith)

Xtr = data_train(:, 1:end-1);
ytr = data_train(:, end);
Xte = data_test(:, 1:end-1);
yte = data_test(:, end);

options.MaxIter = 100000;
calc_error = @(actual, prediction)(sum(actual ~= prediction)/length(prediction));

% generate a classifier and get the predictions on the test data 
svm_struct = svmtrain(Xtr, ytr, ...
  'kernel_function', 'rbf', ...
  'rbf_sigma', bandwith, ...
  'boxconstraint', C, ...
  'method', 'SMO', ...
  'tolkkt', 1e-4, ...
  'kktviolationlevel', 0.15, ...
  'options', options);
yhat = svmclassify(svm_struct, Xte);
errs = calc_error(yhat, yte);

